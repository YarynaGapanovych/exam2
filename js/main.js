function initMap() {
  map = new google.maps.Map(document.getElementById('map'), {
    center: coords,
    zoom: 13,
    styles
  })

  marker = new google.maps.Marker({
    position: coords,
    map,
    icon: 'images/marker.png'
  })

  info = new google.maps.InfoWindow({
    content
  })

  marker.addListener('click', () => info.open(map, marker))

}

